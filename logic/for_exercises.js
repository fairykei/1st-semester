// numeros pares de 0 a 50
for (cont = 2; cont <= 50; cont += 2) {
  document.write(`${cont} <br>`);
}

// multiplos de 5, do 10 ao 30
for (cont = 10; cont <= 30; cont += 5) {
  document.write(`${cont} <br>`);
}

// numeros impares do 30 ao 0
for (cont = 29; cont > 0; cont -= 2) {
  document.write(`${cont} <br>`);
}
